#include "push_swap.h"

int	size_stack(int argc, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			if (argv[1][i] == ' ')
			{
				j++;
				i++;
			}
		}
	}
	else
		j = argc - 1;
	return (j);
}
